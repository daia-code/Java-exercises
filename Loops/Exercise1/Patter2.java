package ro.ITSchool;

public class Patter2 {
    public static void main(String[] args) {
        System.out.println("Patteer II");
        for (int i = 6; i >= 1; i--) {
            for (int j = 1; j <= i; j++) {
                System.out.print(j + " ");
            }
            System.out.println();

        }
        System.out.println();
    }
}
